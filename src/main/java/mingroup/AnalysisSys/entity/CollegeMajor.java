package mingroup.AnalysisSys.entity;

public class CollegeMajor {
    private int year;
    private String provinceName;
    private String collegeName;
    private float lowestGrade;
    private int lowestRanking;
    private String majorName;
    private float majorGrade;
    private int majorRanking;

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getProvinceName() {
        return provinceName;
    }

    public void setProvinceName(String provinceName) {
        this.provinceName = provinceName;
    }

    public String getCollegeName() {
        return collegeName;
    }

    public void setCollegeName(String collegeName) {
        this.collegeName = collegeName;
    }

    public float getLowestGrade() {
        return lowestGrade;
    }

    public void setLowestGrade(float lowestGrade) {
        this.lowestGrade = lowestGrade;
    }

    public int getLowestRanking() {
        return lowestRanking;
    }

    public void setLowestRanking(int lowestRanking) {
        this.lowestRanking = lowestRanking;
    }

    public String getMajorName() {
        return majorName;
    }

    public void setMajorName(String majorName) {
        this.majorName = majorName;
    }

    public float getMajorGrade() {
        return majorGrade;
    }

    public void setMajorGrade(float majorGrade) {
        this.majorGrade = majorGrade;
    }

    public int getMajorRanking() {
        return majorRanking;
    }

    public void setMajorRanking(int majorRanking) {
        this.majorRanking = majorRanking;
    }

}
